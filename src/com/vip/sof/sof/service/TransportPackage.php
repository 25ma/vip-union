namespace com\vip\sof\sof\service;

class TransportPackage {
	
	static $_TSPEC;
	public $transportNo = null;
	public $oldTransportNo = null;
	public $carriersCode = null;
	public $packageSn = null;
	
	public function __construct($vals=null){
		
		if (!isset(self::$_TSPEC)){
			
			self::$_TSPEC = array(
			1 => array(
			'var' => 'transportNo'
			),
			2 => array(
			'var' => 'oldTransportNo'
			),
			3 => array(
			'var' => 'carriersCode'
			),
			4 => array(
			'var' => 'packageSn'
			),
			
			);
			
		}
		
		if (is_array($vals)){
			
			
			if (isset($vals['transportNo'])){
				
				$this->transportNo = $vals['transportNo'];
			}
			
			
			if (isset($vals['oldTransportNo'])){
				
				$this->oldTransportNo = $vals['oldTransportNo'];
			}
			
			
			if (isset($vals['carriersCode'])){
				
				$this->carriersCode = $vals['carriersCode'];
			}
			
			
			if (isset($vals['packageSn'])){
				
				$this->packageSn = $vals['packageSn'];
			}
			
			
		}
		
	}
	
	
	public function getName(){
		
		return 'TransportPackage';
	}
	
	public function read($input){
		
		$input->readStructBegin();
		while(true){
			
			$schemeField = $input->readFieldBegin();
			if ($schemeField == null) break;
			$needSkip = true;
			
			
			if ("transportNo" == $schemeField){
				
				$needSkip = false;
				$input->readString($this->transportNo);
				
			}
			
			
			
			
			if ("oldTransportNo" == $schemeField){
				
				$needSkip = false;
				$input->readString($this->oldTransportNo);
				
			}
			
			
			
			
			if ("carriersCode" == $schemeField){
				
				$needSkip = false;
				$input->readString($this->carriersCode);
				
			}
			
			
			
			
			if ("packageSn" == $schemeField){
				
				$needSkip = false;
				$input->readI32($this->packageSn); 
				
			}
			
			
			
			if($needSkip){
				
				\Osp\Protocol\ProtocolUtil::skip($input);
			}
			
			$input->readFieldEnd();
		}
		
		$input->readStructEnd();
		
		
		
	}
	
	public function write($output){
		
		$xfer = 0;
		$xfer += $output->writeStructBegin();
		
		if($this->transportNo !== null) {
			
			$xfer += $output->writeFieldBegin('transportNo');
			$xfer += $output->writeString($this->transportNo);
			
			$xfer += $output->writeFieldEnd();
		}
		
		
		if($this->oldTransportNo !== null) {
			
			$xfer += $output->writeFieldBegin('oldTransportNo');
			$xfer += $output->writeString($this->oldTransportNo);
			
			$xfer += $output->writeFieldEnd();
		}
		
		
		if($this->carriersCode !== null) {
			
			$xfer += $output->writeFieldBegin('carriersCode');
			$xfer += $output->writeString($this->carriersCode);
			
			$xfer += $output->writeFieldEnd();
		}
		
		
		if($this->packageSn !== null) {
			
			$xfer += $output->writeFieldBegin('packageSn');
			$xfer += $output->writeI32($this->packageSn);
			
			$xfer += $output->writeFieldEnd();
		}
		
		
		$xfer += $output->writeFieldStop();
		$xfer += $output->writeStructEnd();
		return $xfer;
	}
	
}

